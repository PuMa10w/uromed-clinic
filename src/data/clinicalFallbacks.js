function getClinicalArea(disease) {
  if (disease.section === 'pediatric') return 'pediatric';
  return disease.subsection || disease.section || 'urology';
}

function toRedFlag(text) {
  return {
    text,
    urgent: true,
    action: 'Ускоренная клиническая оценка и маршрутизация по тяжести состояния.',
  };
}

export function buildRedFlagsFallback(disease) {
  const area = getClinicalArea(disease);
  const items = redFlagTemplates[area] || redFlagTemplates[disease.section] || redFlagTemplates.functional;
  return {
    isFallback: true,
    title: 'Клинический минимум красных флагов',
    note: 'Блок закрывает обязательный минимум безопасности до профильной редакторской ревизии источников.',
    items: items.map(toRedFlag),
  };
}

export function buildFollowUpFallback(disease) {
  const area = getClinicalArea(disease);
  const schedule = followUpTemplates[area] || followUpTemplates[disease.section] || followUpTemplates.functional;
  return {
    isFallback: true,
    title: 'Минимальный план наблюдения',
    note: 'План требует уточнения после сверки с профильными рекомендациями и локальным клиническим маршрутом.',
    schedule,
    monitoring: [
      'Документировать дату клинической ревизии и использованный guideline-пак.',
      'Обновить раздел при изменении EAU/AUA/CUA/USANZ/российских рекомендаций.',
    ],
  };
}

export function hasRenderableRequiredTabs(disease) {
  return {
    redflags: Boolean(disease.redFlags?.length || buildRedFlagsFallback(disease).items.length),
    followUp: Boolean(disease.followUp || buildFollowUpFallback(disease).schedule.length),
  };
}
