function pickArrayFieldWithMostItems(classification = {}) {
  const keys = [
    'byType',
    'byStage',
    'bySeverity',
    'byEtiology',
    'byCourse',
    'byLocation',
    'byComplications',
  ];

  let bestKey = 'byType';
  let maxLength = Array.isArray(classification.byType) ? classification.byType.length : 0;

  keys.forEach((key) => {
    const currentLength = Array.isArray(classification[key]) ? classification[key].length : 0;
    if (currentLength > maxLength) {
      bestKey = key;
      maxLength = currentLength;
    }
  });

  return bestKey;
}

function ensureClassificationDepth(classification = {}, fallbackClassification = {}) {
  const merged = mergeClassification(classification, fallbackClassification);
  const targetKey = pickArrayFieldWithMostItems(merged);
  const current = Array.isArray(merged[targetKey]) ? merged[targetKey] : [];

  if (current.length >= 3) return merged;

  const genericFallback = mergeSectionLists(current, [
    'Клинически значимый вариант',
    'Умеренно выраженный вариант',
    'Осложненный/высокорисковый вариант',
  ]);

  return {
    ...merged,
    [targetKey]: genericFallback,
  };
}

