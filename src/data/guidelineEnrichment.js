function applyDiseaseSpecificEnrichment(disease, enriched) {
  const profile = DISEASE_SPECIFIC_ENRICHMENT[disease.id];
  if (!profile) return enriched;

  return {
    ...enriched,
    diagnostics: {
      ...enriched.diagnostics,
      primary: mergeSectionLists(enriched.diagnostics?.primary, profile.diagnostics?.primary),
      additional: mergeSectionLists(enriched.diagnostics?.additional, profile.diagnostics?.additional),
    },
    followUp: {
      ...enriched.followUp,
      schedule: mergeSectionLists(enriched.followUp?.schedule, profile.followUp?.schedule),
      monitoring: mergeSectionLists(enriched.followUp?.monitoring, profile.followUp?.monitoring),
    },
    redFlags: mergeRedFlags(enriched.redFlags, profile.redFlags),
    patientQuestions: mergeFaq(enriched.patientQuestions, profile.patientQuestions),
    clinicalCases: mergeClinicalCases(enriched.clinicalCases, profile.clinicalCases),
  };
}

export function enrichDiseaseGuidelines(disease) {
  const fallback = getFallbacksForDisease(disease);
  const evidenceMetadata = buildEvidenceMetadata(disease);
  const classification = ensureClassificationDepth(disease.classification, fallback.classification);

  const diagnosticsMerged = disease.diagnostics
    ? {
        ...fallback.diagnostics,
        ...disease.diagnostics,
        steps: mergeDiagnosticSteps(disease.diagnostics.steps, fallback.diagnostics.steps),
        primary: mergeSectionLists(disease.diagnostics.primary, fallback.diagnostics.primary),
        additional: mergeSectionLists(disease.diagnostics.additional, fallback.diagnostics.additional),
      }
    : fallback.diagnostics;
  const diagnostics = ensureDiagnosticsDepth(diagnosticsMerged, disease.subsection);

  const treatmentMerged = disease.treatment
    ? {
        ...disease.treatment,
        conservative: mergeTreatmentSection(disease.treatment.conservative, fallback.treatment.conservative),
        surgical: mergeTreatmentSection(disease.treatment.surgical, fallback.treatment.surgical),
        metaphylaxis: mergeSectionLists(disease.treatment.metaphylaxis, fallback.treatment.metaphylaxis),
      }
    : fallback.treatment;
  const treatment = ensureTreatmentDepth(treatmentMerged, disease.subsection);

  const guidelines = ensureGuidelineRecency(
    mergeGuidelineBlock(disease.guidelines, fallback.guidelines),
    disease.subsection
  );

  const followUpMerged = disease.followUp
    ? {
        ...fallback.followUp,
        ...disease.followUp,
        schedule: mergeSectionLists(disease.followUp.schedule, fallback.followUp.schedule),
        monitoring: mergeSectionLists(disease.followUp.monitoring, fallback.followUp.monitoring),
      }
    : fallback.followUp;
  const followUp = ensureFollowUpDepth(followUpMerged);

  const prognosisMerged = disease.prognosis
    ? {
        ...fallback.prognosis,
        ...disease.prognosis,
        factors: mergeSectionLists(disease.prognosis.factors, fallback.prognosis.factors),
      }
    : fallback.prognosis;
  const prognosis = ensurePrognosisDepth(prognosisMerged);

  const redFlags = ensureRedFlagsDepth(mergeRedFlags(disease.redFlags, fallback.redFlags));
  const whenToRefer = ensureReferralDepth(mergeReferral(disease.whenToRefer, fallback.whenToRefer));
  const patientQuestions = mergeFaq(disease.patientQuestions, fallback.patientQuestions);
  const clinicalCases = mergeClinicalCases(disease.clinicalCases, fallback.clinicalCases);
  const patientRecommendations = mergeSectionLists(disease.patientRecommendations, fallback.patientRecommendations);
  const lifestyleAdvice = mergeSectionLists(disease.lifestyleAdvice, fallback.lifestyleAdvice);
  const nutritionAdvice = mergeSectionLists(disease.nutritionAdvice, fallback.nutritionAdvice);
  const patientGuidance = ensurePatientGuidanceDepth({
    patientRecommendations,
    lifestyleAdvice,
    nutritionAdvice,
  });

  return applyDiseaseSpecificEnrichment(disease, {
    ...disease,
    classification,
    quickSummary: ensureQuickSummaryDepth(disease, disease.quickSummary),
    symptoms: ensureSymptomDepth(disease, disease.symptoms),
    complications: ensureComplicationDepth(disease, disease.complications),
    diagnostics,
    treatment,
    guidelines,
    followUp,
    prognosis,
    redFlags,
    whenToRefer,
    patientQuestions,
    patientRecommendations: patientGuidance.patientRecommendations,
    lifestyleAdvice: patientGuidance.lifestyleAdvice,
    nutritionAdvice: patientGuidance.nutritionAdvice,
    differentialDiagnosis: ensureDifferentialDepth(disease, disease.differentialDiagnosis),
    labNorms: ensureLabNormsDepth(disease, disease.labNorms),
    clinicalCases: ensureClinicalCaseDepth(disease, clinicalCases),
    lastReviewed: disease.lastReviewed || evidenceMetadata.lastReviewed,
    evidenceVersion: disease.evidenceVersion || evidenceMetadata.evidenceVersion,
    reviewStatus: disease.reviewStatus || evidenceMetadata.reviewStatus,
  });
}

