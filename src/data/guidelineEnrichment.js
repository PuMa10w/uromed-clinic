function getFallbacksForDisease(disease) {
  const sectionFallback = SECTION_FALLBACKS[disease.section] || {};
  const subsectionFallback = disease.subsection ? (SUBSECTION_FALLBACKS[disease.subsection] || {}) : {};
  const sectionContentFallback = SECTION_CONTENT_FALLBACKS[disease.section] || {};
  const subsectionContentFallback = disease.subsection ? (SUBSECTION_CONTENT_FALLBACKS[disease.subsection] || {}) : {};
  const sectionClassificationFallback = SECTION_CLASSIFICATION_FALLBACKS[disease.section] || {};
  const subsectionClassificationFallback = disease.subsection ? (SUBSECTION_CLASSIFICATION_FALLBACKS[disease.subsection] || {}) : {};
  const sectionPatientGuidanceFallback = SECTION_PATIENT_GUIDANCE_FALLBACKS[disease.section] || {};
  const subsectionPatientGuidanceFallback = disease.subsection ? (SUBSECTION_PATIENT_GUIDANCE_FALLBACKS[disease.subsection] || {}) : {};

  return {
    classification: mergeClassification(sectionClassificationFallback, subsectionClassificationFallback),
    diagnostics: { ...(sectionFallback.diagnostics || {}), ...(subsectionFallback.diagnostics || {}) },
    treatment: {
      conservative: [
        ...((sectionFallback.treatment?.conservative) || []),
        ...((subsectionFallback.treatment?.conservative) || []),
      ],
      surgical: [
        ...((sectionFallback.treatment?.surgical) || []),
        ...((subsectionFallback.treatment?.surgical) || []),
      ],
      metaphylaxis: mergeSectionLists(sectionFallback.treatment?.metaphylaxis, subsectionFallback.treatment?.metaphylaxis),
    },
    guidelines: mergeGuidelineBlock(sectionFallback.guidelines, subsectionFallback.guidelines),
    followUp: {
      schedule: mergeSectionLists(sectionFallback.followUp?.schedule, subsectionFallback.followUp?.schedule),
      monitoring: mergeSectionLists(sectionFallback.followUp?.monitoring, subsectionFallback.followUp?.monitoring),
    },
    prognosis: {
      ...sectionFallback.prognosis,
      ...subsectionFallback.prognosis,
      factors: mergeSectionLists(sectionFallback.prognosis?.factors, subsectionFallback.prognosis?.factors),
    },
    redFlags: mergeRedFlags(sectionFallback.redFlags, subsectionFallback.redFlags),
    whenToRefer: mergeReferral(sectionFallback.whenToRefer, subsectionFallback.whenToRefer),
    patientQuestions: mergeFaq(sectionContentFallback.patientQuestions, subsectionContentFallback.patientQuestions),
    clinicalCases: mergeClinicalCases(sectionContentFallback.clinicalCases, subsectionContentFallback.clinicalCases),
    patientRecommendations: mergeSectionLists(
      sectionPatientGuidanceFallback.patientRecommendations,
      subsectionPatientGuidanceFallback.patientRecommendations
    ),
    lifestyleAdvice: mergeSectionLists(
      sectionPatientGuidanceFallback.lifestyleAdvice,
      subsectionPatientGuidanceFallback.lifestyleAdvice
    ),
    nutritionAdvice: mergeSectionLists(
      sectionPatientGuidanceFallback.nutritionAdvice,
      subsectionPatientGuidanceFallback.nutritionAdvice
    ),
  };
}

