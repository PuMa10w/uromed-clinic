function uniqueItems(items) {
  return [...new Set(items.filter(Boolean))];
}

function mergeSectionLists(primary = [], fallback = []) {
  return uniqueItems([...(Array.isArray(primary) ? primary : []), ...(Array.isArray(fallback) ? fallback : [])]);
}

function mergeTreatmentSection(primary = [], fallback = []) {
  const normalizedPrimary = Array.isArray(primary) ? primary : [];
  const normalizedFallback = Array.isArray(fallback) ? fallback : [];
  const fallbackByTitle = new Map(
    normalizedFallback.map((section) => [section.title || '', section])
  );

  const mergedPrimary = normalizedPrimary.map((section) => {
    const sameTitleFallback = fallbackByTitle.get(section.title || '');
    return {
      ...section,
      items: mergeSectionLists(section.items, sameTitleFallback?.items),
    };
  });

  const primaryTitles = new Set(normalizedPrimary.map((section) => section.title || ''));
  const missingFallbackSections = normalizedFallback
    .filter((section) => !primaryTitles.has(section.title || ''))
    .map((section) => ({
      ...section,
      items: mergeSectionLists(section.items, []),
    }));

  return [...mergedPrimary, ...missingFallbackSections];
}

function mergeDiagnosticSteps(primary = [], fallback = []) {
  const normalizedPrimary = Array.isArray(primary) ? primary : [];
  const normalizedFallback = Array.isArray(fallback) ? fallback : [];
  const merged = [...normalizedPrimary, ...normalizedFallback];
  const seen = new Set();

  const deduped = merged.filter((item) => {
    if (!item || typeof item !== 'object') return false;
    const key = `${item.text || ''}|${Boolean(item.main)}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });

  return deduped.map((item, index) => ({
    ...item,
    step: index + 1,
  }));
}

function normalizeClassificationShape(value) {
  if (!value) return {};
  if (Array.isArray(value)) {
    return { title: 'Классификация', byType: value };
  }
  if (typeof value === 'string') {
    return { title: 'Классификация', byType: [value] };
  }
  return { ...value };
}

function mergeClassification(primary = {}, fallback = {}) {
  const normalizedPrimary = normalizeClassificationShape(primary);
  const normalizedFallback = normalizeClassificationShape(fallback);
  const result = { ...normalizedFallback, ...normalizedPrimary };

  [
    'items',
    'byType',
    'byStage',
    'bySeverity',
    'byEtiology',
    'byCourse',
    'byLocation',
    'byOnset',
    'byPhase',
    'byLevel',
    'byGrade',
    'byHistology',
    'byRisk',
    'byComplications',
  ].forEach((key) => {
    result[key] = mergeSectionLists(normalizedPrimary[key], normalizedFallback[key]);
  });

  if (normalizedPrimary.tnm || normalizedFallback.tnm) {
    result.tnm = {
      ...(normalizedFallback.tnm || {}),
      ...(normalizedPrimary.tnm || {}),
      T: mergeSectionLists(normalizedPrimary.tnm?.T, normalizedFallback.tnm?.T),
      N: mergeSectionLists(normalizedPrimary.tnm?.N, normalizedFallback.tnm?.N),
      M: mergeSectionLists(normalizedPrimary.tnm?.M, normalizedFallback.tnm?.M),
    };
  }

  return result;
}

function mergeGuidelineBlock(primary = {}, fallback = {}) {
  const result = { ...fallback, ...primary };

  ['eau', 'aua', 'ru', 'ua'].forEach((key) => {
    if (primary[key] || fallback[key]) {
      result[key] = {
        ...(fallback[key] || {}),
        ...(primary[key] || {}),
        keyPoints: mergeSectionLists(primary[key]?.keyPoints, fallback[key]?.keyPoints),
      };
    }
  });

  result.consensus = mergeSectionLists(primary.consensus, fallback.consensus);
  return result;
}

function mergeRedFlags(primary = [], fallback = []) {
  const normalizedPrimary = Array.isArray(primary) ? primary : [];
  const normalizedFallback = Array.isArray(fallback) ? fallback : [];
  const merged = [...normalizedPrimary, ...normalizedFallback];
  const seen = new Set();

  return merged.filter((item) => {
    const key = typeof item === 'string' ? item : `${item.text || ''}|${item.action || ''}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function mergeReferral(primary = {}, fallback = {}) {
  const result = { ...fallback, ...primary };
  ['toUrologist', 'toNephrologist', 'toOncologist', 'toEmergency'].forEach((key) => {
    result[key] = mergeSectionLists(primary[key], fallback[key]);
  });
  return result;
}

function asObjectArray(value) {
  if (!value) return [];
  if (Array.isArray(value)) return value;
  if (typeof value === 'object') return Object.values(value);
  return [];
}

function asTextArray(value) {
  if (!value) return [];
  if (Array.isArray(value)) return value;
  if (typeof value === 'string') return [value];
  if (typeof value === 'object') return Object.values(value);
  return [];
}

function normalizeFaqItem(item) {
  if (!item) return null;
  if (typeof item === 'string') {
    return {
      q: 'Что важно уточнить у врача?',
      a: item,
    };
  }

  return {
    q: item.q || item.question || 'Что важно уточнить у врача?',
    a: item.a || item.answer || 'Следуйте персональному плану, согласованному с лечащим врачом.',
  };
}

function mergeFaq(primary = [], fallback = []) {
  const merged = [...asObjectArray(primary), ...asObjectArray(fallback)].map(normalizeFaqItem).filter(Boolean);
  const seen = new Set();

  return merged.filter((item) => {
    const key = `${item.q}|${item.a}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function normalizeClinicalCase(item, index = 0) {
  if (!item) return null;
  if (typeof item === 'string') {
    return {
      title: `Клинический случай №${index + 1}`,
      findings: item,
    };
  }

  return {
    title: item.title || `Клинический случай №${index + 1}`,
    patient: item.patient || '',
    complaint: item.complaint || '',
    findings: item.findings || item.case || '',
    diagnosis: item.diagnosis || '',
    treatment: item.treatment || '',
    outcome: item.outcome || '',
    lesson: item.lesson || '',
  };
}

function mergeClinicalCases(primary = [], fallback = []) {
  const merged = [...asObjectArray(primary), ...asObjectArray(fallback)]
    .map((item, index) => normalizeClinicalCase(item, index))
    .filter(Boolean);
  const seen = new Set();

  return merged.filter((item) => {
    const key = `${item.title}|${item.complaint}|${item.findings}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

