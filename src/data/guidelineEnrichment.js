function getQualityKey(disease) {
  return disease.subsection || disease.section || 'default';
}

function ensureSymptomDepth(disease, current = []) {
  const existing = mergeSectionLists(asTextArray(current).map((item) => (
    typeof item === 'string' ? item : item.text || item.name || ''
  )), []);
  const key = getQualityKey(disease);
  const fallback = QUALITY_SYMPTOM_FALLBACKS[key] || QUALITY_SYMPTOM_FALLBACKS.default;
  return mergeSectionLists(existing, fallback).slice(0, Math.max(5, existing.length));
}

